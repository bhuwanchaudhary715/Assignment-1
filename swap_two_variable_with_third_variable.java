

public class swap_two_variable_with_third_variable {

    public static void main(String[] args) {

        int x=2;
        int y=3;
        int temp;

//        before swapping
        System.out.println("the value of x is:" +x);
        System.out.println("the value of y is:" +y);
        temp=x;
        x=y;
        y=temp;

//        after swapping
        System.out.println("the value of x is:" +x);
        System.out.println("the value of y is:" +y);




    }


}
